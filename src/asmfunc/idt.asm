[bits 32]
%define ERROR_CODE nop
%define ZERO push 0

extern idt_table

section .data
global intr_entry_table

intr_entry_table:
%macro VECTOR 2
section .text
intr%1entry:
	%2
	push ds
	push es
	push fs
	push gs
	pushad
	
	;8259A设置
	mov al,0x20
	out 0xa0,al
	out 0x20,al
	
	push %1
	call [idt_table + %1 * 4]
	jmp intr_exit

section .data
	dd intr%1entry
%endmacro

section .text
global intr_exit
intr_exit:
	add esp,4
	popad
	pop gs
	pop fs
	pop es
	pop ds
	add esp,4
	iretd
	
